function updateGrandTotal() {
    const participantCount = parseInt(commonParticipantsInput?.value || '0') || 0;
    document.getElementById('summary-participants').textContent = participantCount;

    let regTotal = 0;
    for (const key in selectedOptions) {
      const { price } = selectedOptions[key];
      regTotal += price;
    }

    const regTotalPrice = regTotal * participantCount;
    document.getElementById('summary-registration').textContent = regTotal.toFixed(2);
    document.getElementById('summary-total-registration').textContent = regTotalPrice.toFixed(2);

    let total = regTotalPrice;
    const tableBody = document.getElementById('summary-table-body');
    if (!tableBody) return;

    // Remove previous accommodation and accompanying rows if any
    const oldAccRow = document.getElementById('summary-accommodation-row');
    if (oldAccRow) oldAccRow.remove();

    const oldAccPersonRow = document.getElementById('summary-accompanying-row');
    if (oldAccPersonRow) oldAccPersonRow.remove();
    
    const oldExhibitorsRow = document.getElementById('summary-exhibitors-row');
    if (oldExhibitorsRow) oldExhibitorsRow.remove();
    
    const oldSponsorsRow = document.getElementById('summary-sponsors-row');
    if (oldSponsorsRow) oldSponsorsRow.remove();

    const totalPriceRow = tableBody.lastElementChild;

    // Add Accommodation Row
    if (accommodationNights > 0 && Object.keys(accommodationSelections).length > 0) {
      let accTotal = 0;
      let accDetails = [];

      for (const label in accommodationSelections) {
        const price = accommodationSelections[label];
        accTotal += price * accommodationNights;
        accDetails.push(`${label} (${accommodationNights} nights)`);
      }

      total += accTotal;

      const accRow = document.createElement('tr');
      accRow.id = 'summary-accommodation-row';
      accRow.innerHTML = `
        <td><strong>Accommodation:</strong><br><small>${accDetails.join('<br>')}</small></td>
        <td class="text-right">$${accTotal.toFixed(2)}</td>
      `;
      tableBody.insertBefore(accRow, totalPriceRow);
    }
    
    if(Object.keys(exhibitorSelections).length > 0) {
        let exhibitorTotal = 0;
        let exhibitorDetails = [];
        
        for (const label in exhibitorSelections) {
            const price = exhibitorSelections[label];
            exhibitorTotal += price;
            exhibitorDetails.push(`${label}`);
        }

        total += exhibitorTotal;

        const exhibitorRow = document.createElement('tr');
        exhibitorRow.id = 'summary-exhibitors-row';
        exhibitorRow.innerHTML = `
            <td><strong>Exhibitors:</strong></td>
            <td class="text-right">$${exhibitorTotal.toFixed(2)}</td>
        `;
        tableBody.insertBefore(exhibitorRow, totalPriceRow);
    }
    
    if(Object.keys(sponsorsSelections).length > 0) {
        let sponsorsTotal = 0;
        let sponsorsDetails = [];
        
        for (const label in sponsorsSelections) {
            const price = sponsorsSelections[label];
            sponsorsTotal += price;
            sponsorsDetails.push(`${label}`);
        }

        total += sponsorsTotal;

        const sponsorsRow = document.createElement('tr');
        sponsorsRow.id = 'summary-sponsors-row';
        sponsorsRow.innerHTML = `
            <td><small>${sponsorsDetails.join('<br>')}</small></td>
            <td class="text-right">$${sponsorsTotal.toFixed(2)}</td>
        `;
        tableBody.insertBefore(sponsorsRow, totalPriceRow);
    }

    // Add Accompanying Person Row
    if (accompanyingCount > 0) {
      const accPersonTotal = accompanyingCount * ACCOMPANYING_PRICE;
      total += accPersonTotal;

      const accPersonRow = document.createElement('tr');
      accPersonRow.id = 'summary-accompanying-row';
      accPersonRow.innerHTML = `
        <td><strong>Accompanying Person:</strong><br><small>Count: ${accompanyingCount}</small></td>
        <td class="text-right">$${accPersonTotal.toFixed(2)}</td>
      `;
      tableBody.insertBefore(accPersonRow, totalPriceRow);
    }
    if($("#discount").val()) {
        var finaltotal = total * $("#discount").val();
        $("#total").val(finaltotal.toFixed(2));
    } else {
        $("#total").val(total.toFixed(2));
    }
    // Update grand total
    document.getElementById('summary-grand-total').textContent = total.toFixed(2);
  }
