      const parts = dateStr.split("-");
      return new Date(parts[0], parts[1] - 1, parts[2]); // local midnight
    }
    
    function calculateNightsAndTotal() {
      const checkin = document.getElementById('checkin').value;
      const checkout = document.getElementById('checkout').value;
    
      if (checkin && checkout) {
        const checkinDate = parseDate(checkin);
        const checkoutDate = parseDate(checkout);
    
        const timeDiff = checkoutDate - checkinDate;
        const nights = Math.floor(timeDiff / (1000 * 60 * 60 * 24));
    
        accommodationNights = nights > 0 ? nights : 0;
        document.getElementById('nights').value = accommodationNights;
      } else {
        accommodationNights = 0;
        document.getElementById('nights').value = 0;
      }
    
      updateGrandTotal();
    }

  document.getElementById('checkin').addEventListener('change', calculateNightsAndTotal);
  document.getElementById('checkout').addEventListener('change', calculateNightsAndTotal);

  // Accompanying person logic
  const accompanyingCheckbox = document.getElementById('accompanying-person');
  const accompanyingBox = document.getElementById('accompanying-box');
  const accompanyingInput = document.getElementById('accompanying-count');

  accompanyingCheckbox.addEventListener('change', () => {
    if (accompanyingCheckbox.checked) {
      accompanyingBox.style.display = 'block';
      accompanyingCount = parseInt(accompanyingInput.value) || 1;
    } else {
      accompanyingBox.style.display = 'none';
      accompanyingCount = 0;
    }
    updateGrandTotal();
  });

  accompanyingInput.addEventListener('input', () => {
    accompanyingCount = parseInt(accompanyingInput.value) || 0;
    updateGrandTotal();
  });

